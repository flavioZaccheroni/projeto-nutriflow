class FoodModel {
  final String name;
  final String category;
  final String portion;
  final int calories;
  final double protein;
  final double carbs;
  final double fat;

  const FoodModel({
    required this.name,
    required this.category,
    required this.portion,
    required this.calories,
    required this.protein,
    required this.carbs,
    required this.fat,
  });
}
